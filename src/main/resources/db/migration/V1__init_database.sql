-- ==========================================
-- 1. CÁC BẢNG ĐỘC LẬP (PARENT TABLES)
-- ==========================================

-- Bảng Items
CREATE TABLE items (
                       id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       inventory_qty int4 NOT NULL,
                       "name" varchar(255) NULL,
                       price float8 NOT NULL,
                       url_img varchar(255) NULL
);

-- Bảng Roles
CREATE TABLE roles (
                       id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       role_name varchar(255) NULL
);

-- Bảng Users
CREATE TABLE users (
                       id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       created_at date NULL,
                       email varchar(255) NULL,
                       img_url varchar(255) NULL,
                       "password" varchar(255) NULL,
                       status int2 NULL,
                       CONSTRAINT users_status_check CHECK (((status >= 0) AND (status <= 1)))
);

-- ==========================================
-- 2. CÁC BẢNG PHỤ THUỘC (CHILD TABLES)
-- ==========================================

-- Bảng Cart (Phụ thuộc Users)
CREATE TABLE cart (
                      id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      total_cart float8 NOT NULL,
                      user_id int8 NULL,
                      CONSTRAINT uk_cart_user UNIQUE (user_id),
                      CONSTRAINT fk_cart_user FOREIGN KEY (user_id) REFERENCES users(id)
);

-- Bảng Orders (Phụ thuộc Users)
CREATE TABLE orders (
                        id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        order_date date NULL,
                        total_order float8 NOT NULL,
                        user_id int8 NULL,
                        CONSTRAINT fk_orders_user FOREIGN KEY (user_id) REFERENCES users(id)
);

-- Bảng User_Role (Bảng trung gian: Phụ thuộc Users và Roles)
CREATE TABLE user_role (
                           role_id int8 NOT NULL,
                           user_id int8 NOT NULL,
                           CONSTRAINT user_role_pkey PRIMARY KEY (role_id, user_id),
                           CONSTRAINT fk_ur_user FOREIGN KEY (user_id) REFERENCES users(id),
                           CONSTRAINT fk_ur_role FOREIGN KEY (role_id) REFERENCES roles(id)
);

-- Bảng Cart_Detail (Phụ thuộc Items và Cart)
CREATE TABLE cart_detail (
                             id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             date_add date NULL,
                             purchase_price float8 NOT NULL,
                             quantity int4 NOT NULL,
                             total_item float8 NOT NULL,
                             cart_id int4 NULL,
                             item_id int4 NULL,
                             CONSTRAINT fk_cd_item FOREIGN KEY (item_id) REFERENCES items(id),
                             CONSTRAINT fk_cd_cart FOREIGN KEY (cart_id) REFERENCES cart(id)
);

-- Bảng Order_Detail (Phụ thuộc Items)
-- Lưu ý: Bạn có thể thêm khóa ngoại cho order_id sau này nếu cần
CREATE TABLE order_detail (
                              id int4 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                              purchased_price float8 NOT NULL,
                              quantity int4 NOT NULL,
                              total_item float8 NOT NULL,
                              item_id int4 NULL,
                              CONSTRAINT fk_od_item FOREIGN KEY (item_id) REFERENCES items(id)
);